package Wyllian;

public class Aresta {
	private Vertice origem;
	private Vertice destino;
	
	public Vertice getOrigem() {
		return this.origem;
	}
	
	public void setOrigem(Vertice origin) {
		this.origem = origin;
	}
	
	public Vertice getDestino() {
		return this.destino;
	}
	
	public void setDestino(Vertice destination) {
		this.destino = destination;
	}
}